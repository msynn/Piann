def luas_persegi(sisi):
    return sisi * sisi
    # Kata kunci return berfungsi untuk mengembalikan niai.
    # Nilai yang dikembalikan suatu fungsi,
    # bisa kita olah kembali untuk berbagai kebutuhan.

# Tidak menghasilkan output
luas_persegi(10)

# Menghasilkan output
print(f'Luas persegi dengan sisi 4 cm :{luas_persegi(4)}cm')