a = 10
b = 3

# Operasi penambahan +
hasil = a + b
print(a,'+',b,'=',hasil)

#Operasi Pengurangan -
hasil = a - b
print(a,'-',b,'=', hasil)

#Operaso Perkalian *
hasil = a * b
print(a,'*',b,'=', hasil)

#Operasi Pembagian /
hasil = a / b
print(a,'/',b,'=', hasil)

#Operaso exponen (pangkat) **
hasil = a ** b
print(a,'**',b,'=', hasil)